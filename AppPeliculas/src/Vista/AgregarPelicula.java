    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed

        String titulo,director,codigo,categoria,formato;
        int duracion;
        boolean estreno = false;
        
        codigo = jTextField1.getText();
        titulo = jTextField2.getText();
        director = jTextField4.getText();
        duracion = Integer.parseInt(jTextField3.getText());
        if(jRadioButton1.isSelected()==true){
            formato ="V";
        }else{
            formato = "B";
        }
        
        
        categoria = String.valueOf(jComboBox1.getSelectedItem());
        
       if (titulo.equalsIgnoreCase("") || codigo.equalsIgnoreCase("") 
            || director.equalsIgnoreCase("") || String.valueOf(duracion).equalsIgnoreCase("") || categoria.equalsIgnoreCase("seleccione") 
        ){
            JOptionPane.showMessageDialog(rootPane,"TODOS LOS CAMPOS SON OBLIGATORIOS");
        }else if(this.existeCodigo(codigo) == true){
            JOptionPane.showMessageDialog(rootPane,"El codigo esta repetido");
        }
       else {   
            Pelicula pelicula = new Pelicula(titulo, director, categoria, codigo, formato, duracion, estreno);
            Registro.agregarPelicula(pelicula);
            JOptionPane.showMessageDialog(rootPane,"Pelicula agregada.");
           }
    }//GEN-LAST:event_BotonAgregarActionPerformed

    public boolean existeCodigo(String cod){
        String codigoObtenido = "";
        String sql = "SELECT codigo FROM pelicula WHERE codigo = '"+cod+"'";
        try {
            Connection conexion = Conexion.getConexion();
            Statement st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                codigoObtenido = rs.getString(1);
            }
            if(cod.equalsIgnoreCase(codigoObtenido)){
                return true;
            }else{
                return false;
            }
    } catch (Exception e) {
            System.out.println("Error al verificar codigo");
            System.out.println(e.getMessage());
            return false;
    }
    }
    
